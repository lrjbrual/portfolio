import Toastr from 'toastr';

function loadToastr() {
  toastr.options = ({
    “closeButton”: true,
    “debug”: false,
    “positionClass”: “toast-bottom-right”,
    “onclick”: null,
    “showDuration”: “300”,
    “hideDuration”: “1500”,
    “timeOut”: “5000”,
    “extendedTimeOut”: “1000”,
    “showEasing”: “swing”,
    “hideEasing”: “linear”,
    “showMethod”: “fadeIn”,
    “hideMethod”: “fadeOut”
    });
}

export { loadToastr };